
create database lagoinhalisboa;
use lagoinhalisboa;

CREATE TABLE membro (
  ID_MEMBRO bigint(20) NOT NULL AUTO_INCREMENT,
  NOME varchar(50) NOT NULL,
  NUMERO_DOCUMENTO varchar(15) NOT NULL,
  DATA_VALIDADE_DOCUMENTO date DEFAULT NULL,
  NACIONALIDADE varchar(10) DEFAULT NULL,
  SEXO varchar(10) NOT NULL,
  LOGRADOURO varchar(200) NOT NULL,
  ZONA varchar(10) NOT NULL,
  CIDADE varchar(30) NOT NULL,
  PAIS varchar(10) NOT NULL,
  EMAIL varchar(50) DEFAULT NULL,
  FOTO blob,
  NOME_PAI varchar(50) DEFAULT NULL,
  NOME_MAE varchar(50) DEFAULT NULL,
  ESTADO_CIVIL varchar(10) NOT NULL,
  NOME_CONJUGE varchar(50) DEFAULT NULL,
  DATA_CASAMENTO date DEFAULT NULL,
  QTD_FILHOS int(11) DEFAULT NULL,
  DATA_BATISMO date DEFAULT NULL,
  IGREJA_BATISMO varchar(100) DEFAULT NULL,
  GC bigint(20) DEFAULT NULL,
  PRIMARY KEY (ID_MEMBRO),
  KEY FK_GC (GC),
  CONSTRAINT FK_GC FOREIGN KEY (GC) REFERENCES gc (ID_GC)
);
